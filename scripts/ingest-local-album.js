#!/usr/bin/env node
'use strict'
/**
 * ingest-local-album.js
 * Run this on the machine where the audio files actually live (your PC).
 * Uploads every audio file in a local folder to R2 as one new album,
 * one file at a time: probe duration -> upload -> verify -> (optionally)
 * delete local -> next. Writes a manifest locally AND uploads a copy of
 * it to R2 (next to the audio) so the production server can build the
 * album/song pages from it without needing the files themselves.
 *
 * Track number + title are derived from the filename:
 *   "01 - Song Title.mp3"      -> num 1,  title "Song Title"
 *   "01_song_title.mp3"        -> num 1,  title "song title" (underscores -> spaces)
 *   "Song Title.mp3"           -> num <alphabetical order>, title "Song Title"
 *
 * Usage:
 *   node scripts/ingest-local-album.js --dir "/home/noiraciel/Metal" --album metal --title "Metal" [--delete]
 *
 * Requires .env.local with R2 credentials (copy from the VPS) and ffprobe
 * on PATH. Does NOT delete local files unless --delete is passed — this is
 * irreplaceable personal content, not regenerable like the AI catalogue.
 */

const fs   = require('fs')
const path = require('path')
const { execFileSync } = require('child_process')
const r2   = require('./lib/r2-client')

r2.loadEnv()

function arg(name, def = null) {
  const i = process.argv.indexOf(`--${name}`)
  return i === -1 ? def : process.argv[i + 1]
}
const has = (name) => process.argv.includes(`--${name}`)

const SRC_DIR    = arg('dir')
const ALBUM_SLUG = arg('album')
const ALBUM_TITLE = arg('title', ALBUM_SLUG)
const DELETE_LOCAL = has('delete')

if (!SRC_DIR || !ALBUM_SLUG) {
  console.error('Usage: node scripts/ingest-local-album.js --dir "<folder>" --album <slug> --title "<Album Title>" [--delete]')
  process.exit(1)
}
if (!fs.existsSync(SRC_DIR)) {
  console.error(`✗ Directory not found: ${SRC_DIR}`)
  process.exit(1)
}

const SUPPORTED = /\.(mp3|wav|flac|aiff?|m4a|ogg)$/i
const DIR_NAME  = ALBUM_TITLE.replace(/[^\w\s-]/g, '').trim().replace(/\s+/g, '_')

function slugify(text) {
  return text.toLowerCase().replace(/['']/g, '').replace(/[^a-z0-9]+/g, '-').replace(/^-+|-+$/g, '')
}

function parseFilename(filename, fallbackIndex) {
  const stem = filename.replace(/\.[^.]+$/, '')
  const m = stem.match(/^(\d+)[\s_.\-]+(.+)$/)
  const rawTitle = m ? m[2].replace(/_/g, ' ').trim() : stem.replace(/_/g, ' ').trim()
  const num = m ? parseInt(m[1], 10) : fallbackIndex

  // Auto-generated filenames (bare UUIDs, "image_<timestamp>_<n>") carry no
  // real title — fall back to a generic numbered label instead of dumping
  // the raw hex/id string onto the page as a song title.
  const lettersOnly = rawTitle.replace(/[^a-z]/gi, '')
  const looksAutoGenerated = /^[a-f0-9]{20,}$/i.test(rawTitle.replace(/\s/g, '')) || lettersOnly.length < 3
  const title = looksAutoGenerated ? null : rawTitle
  return { num, title, autoGenerated: looksAutoGenerated }
}

function probeDuration(filePath) {
  const out = execFileSync('ffprobe', [
    '-v', 'error', '-show_entries', 'format=duration', '-of', 'default=noprint_wrappers=1:nokey=1', filePath,
  ], { encoding: 'utf-8' })
  return parseFloat(out.trim())
}

function formatDuration(seconds) {
  if (!seconds || !isFinite(seconds)) return ''
  const m = Math.floor(seconds / 60)
  const s = Math.round(seconds % 60)
  return `${m}:${String(s).padStart(2, '0')}`
}

function walk(dir) {
  const out = []
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    const full = path.join(dir, entry.name)
    if (entry.isDirectory()) out.push(...walk(full))
    else if (SUPPORTED.test(entry.name)) out.push(full)
  }
  return out
}

const MANIFEST_PATH = path.join(__dirname, '..', 'data', 'local-albums', ALBUM_SLUG, 'manifest.json')

function loadManifest() {
  try { return JSON.parse(fs.readFileSync(MANIFEST_PATH, 'utf-8')) } catch { return { slug: ALBUM_SLUG, title: ALBUM_TITLE, dirName: DIR_NAME, tracks: [] } }
}
function saveManifest(m) {
  fs.mkdirSync(path.dirname(MANIFEST_PATH), { recursive: true })
  fs.writeFileSync(MANIFEST_PATH, JSON.stringify(m, null, 2))
}

function naturalCompare(a, b) {
  return path.basename(a).localeCompare(path.basename(b), undefined, { numeric: true, sensitivity: 'base' })
}

async function main() {
  const files = walk(SRC_DIR).sort(naturalCompare)
  if (files.length === 0) {
    console.error(`✗ No audio files found in ${SRC_DIR}`)
    process.exit(1)
  }
  console.log(`Found ${files.length} audio file(s) in ${SRC_DIR}\n`)

  const manifest = loadManifest()
  const doneSlugs = new Set(manifest.tracks.map((t) => t.slug))

  let uploaded = 0, skipped = 0
  for (let i = 0; i < files.length; i++) {
    const filePath = files[i]
    const filename = path.basename(filePath)
    const parsed = parseFilename(filename, i + 1)
    const num = parsed.num
    const title = parsed.title ?? `${ALBUM_TITLE} — Track ${num}`
    const slug = slugify(title)

    if (doneSlugs.has(slug)) {
      console.log(`⏭  [${num}] ${title} — already ingested`)
      skipped++
      continue
    }

    const disk = r2.checkDiskSpace(300)
    if (!disk.ok) { console.error(`✗ STOPPING — low disk (${disk.freeMb}MB)`); process.exit(1) }

    console.log(`↑  [${num}] ${title}`)
    let duration = null
    try { duration = probeDuration(filePath) } catch (e) { r2.warn(`ffprobe failed: ${e.message}`) }

    const ext = path.extname(filePath).slice(1).toLowerCase()
    const numStr = String(num).padStart(2, '0')
    const r2Key = `music/${DIR_NAME}/audio/${numStr}_${slug}.${ext}`

    let migration
    try {
      migration = await r2.migrateFile(filePath, r2Key, { deleteLocal: DELETE_LOCAL })
    } catch (e) {
      console.error(`\n✗ STOPPING — upload/verify failed for "${title}": ${e.message}`)
      process.exit(1)
    }

    manifest.tracks.push({
      num, slug, title,
      filename: `${numStr}_${slug}.${ext}`,
      duration, durationFormatted: formatDuration(duration),
      audioUrl: migration.r2Url, r2Key,
      format: ext.toUpperCase(),
    })
    saveManifest(manifest)
    uploaded++
    if (DELETE_LOCAL && !migration.deletedLocal) {
      r2.warn(`uploaded+verified but local file could not be deleted: ${filePath}`)
    }
  }

  manifest.tracks.sort((a, b) => a.num - b.num)
  saveManifest(manifest)

  // Upload a copy of the manifest itself to R2 so the server can fetch it
  // directly without needing the original files or a git sync.
  await r2.uploadFile(MANIFEST_PATH, `music/${DIR_NAME}/ingest-manifest.json`)

  console.log(`\nDone. ${uploaded} uploaded, ${skipped} already done.`)
  console.log(`Manifest: ${MANIFEST_PATH}`)
  console.log(`Manifest on R2: ${r2.PUBLIC_URL}/music/${DIR_NAME}/ingest-manifest.json`)
  if (!DELETE_LOCAL) {
    console.log(`\nLocal files were kept (pass --delete to remove them after verifying R2 has everything).`)
  }
}

main().catch((e) => { console.error('FAILED:', e.message); process.exit(1) })
